n= input ('Digite algo')
print(n.isnumeric()) #para perguntar se é um número
print (n.isalpha()) #para perguntar se é letra
print (n.isalnum()) #para perguntar se é letra ou número
print (n.isupper()) #para perguntar se é só com letras maiúsculas
print (n.isdecimal()) #para perguntar se é decimal
print (n.isdigit()) #para perguntar se é um digito
print (n.islower()) #para perguntar se é apenas letras minúsculas
print (n.isidentifier()) #para perguntar se é um identificador válido
print (n.isspace()) #para verificar se tem espaço em branco
